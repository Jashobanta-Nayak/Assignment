package EncapsulationPrograms;

public class Employee {
	private String emName;
	private String emDesig;
	private double emSal;
	private String emCity;
	private int emPin;
	private String emProj;
	public String getEmName() {
		return emName;
	}
	public void setEmName(String emName) {
		this.emName = emName;
	}
	public String getEmDesig() {
		return emDesig;
	}
	public void setEmDesig(String emDesig) {
		this.emDesig = emDesig;
	}
	public double getEmSal() {
		return emSal;
	}
	public void setEmSal(double emSal) {
		this.emSal = emSal;
	}
	public String getEmCity() {
		return emCity;
	}
	public void setEmCity(String emCity) {
		this.emCity = emCity;
	}
	public int getEmPin() {
		return emPin;
	}
	public void setEmPin(int emPin) {
		this.emPin = emPin;
	}
	public String getEmProj() {
		return emProj;
	}
	public void setEmProj(String emProj) {
		this.emProj = emProj;
	}
	
	

}
